/**
 * Get related services for a given page path
 * @param {string} path - The page path (e.g., "/residency-spain")
 * @param {Function} t - Translation function from useTranslation
 * @returns {Array} Array of service objects with title, href, and description
 */
export function getRelatedServices(path, t) {
  const services = relatedServicesMap[path.replace(/^\//, "")] || []
  
  return services.map(service => ({
    title: t(service.titleKey),
    href: service.href,
    description: service.descriptionKey ? t(service.descriptionKey) : null
  }))
}

